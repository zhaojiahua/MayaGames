#向量类(一个三维向量类)
class ZVector:
    def __init__(self,ix,iy,iz):
        self.x=ix
        self.y=iy
        self.z=iz

#矩阵类(一个3X3矩阵类,用于计算三维向量的旋转)
class ZMatrix3X3:
    def __init__(self,v1,v2,v3):
        v1=ZVector(0)
        v2=ZVector(0)
        v3=ZVector(0)